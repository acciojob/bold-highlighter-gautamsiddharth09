function highlight() {
    //Write your code here
	let over = document.querySelectorAll("strong");
	over.forEach(element => {
		element.style.color = "rgb(0, 128, 0)";
	})


}


function return_normal() {
    //Write your code here
	let out = document.querySelectorAll("strong");
	out.forEach(element => {
		element.style.color = "rgb(0, 0, 0)";
	})

}






